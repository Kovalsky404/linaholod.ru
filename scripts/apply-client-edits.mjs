async function run() {
  console.log(`→ Проект ${projectId} / ${dataset}\n`);

  // 1) Услуги: полная замена. Удаляем старые, создаём 5 новых.
  await client.delete({ query: '*[_type == "service"]' });
  const tx = client.transaction();
  SERVICES.forEach((s, i) =>
    tx.create({
      _id: `service-${i + 1}`,
      _type: "service",
      title: s.title,
      price: s.price,
      description: s.description,
      order: i,
    }),
  );
  await tx.commit();
  console.log(`✓ Услуги заменены: ${SERVICES.length} шт.`);

  // 2) siteSettings: PATCH текстов (соцсети/фото сохраняются).
  await client.createIfNotExists({
    _id: "siteSettings",
    _type: "siteSettings",
  });
  await client
    .patch("siteSettings")
    .set({
      aboutTitle: "Обо мне",
      aboutText: ABOUT_TEXT,
      whyMeTitle: "Почему я?",
      whyMeText: WHYME_TEXT,
      servicesTerms: SERVICES_TERMS,
    })
    .commit();
  console.log("✓ Тексты «Обо мне», «Почему я?» и условия услуг обновлены");

  console.log(
    "\nГотово. Откройте сайт — контент подтянется (ISR ≤60с) или сразу при ревалидации.",
  );
}

run().catch((err) => {
  console.error("❌ Ошибка:", err.message || err);
  process.exit(1);
});
